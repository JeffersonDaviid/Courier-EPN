    public void actualizarTabla(String[][] datosRegistro){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        //Limpiamos la tabla
        model.setRowCount(0);
        for (int col = 0; col < datosRegistro.length; col++) {
            model.addRow(datosRegistro[col]);
        }
    }
